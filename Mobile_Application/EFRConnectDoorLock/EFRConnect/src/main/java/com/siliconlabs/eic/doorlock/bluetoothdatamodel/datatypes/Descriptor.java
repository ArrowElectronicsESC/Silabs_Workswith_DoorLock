package com.siliconlabs.eic.doorlock.bluetoothdatamodel.datatypes;

import java.util.UUID;

// Descriptor - It's wrapper for <Descriptor> xml tag
public class Descriptor {

    private String name;
    private String type;
    private UUID uuid;

    public Descriptor() {

    }

    public Descriptor(String name, String type, UUID uuid) {
        this.name = name;
        this.type = type;
        this.uuid = uuid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public UUID getUuid() {
        return uuid;
    }

    public void setUuid(UUID uuid) {
        this.uuid = uuid;
    }
}
